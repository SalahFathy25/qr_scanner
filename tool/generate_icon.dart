import 'dart:io';

void main() {
  // Create simple placeholder icon files (1x1 transparent PNG)
  // Replace with your actual app icon before publishing
  final dir = Directory('assets/icons');
  if (!dir.existsSync()) dir.createSync(recursive: true);

  // Minimal valid PNG (1x1 white pixel)
  final png = [
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG header
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
    0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, // IDAT chunk
    0x54, 0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00,
    0x00, 0x00, 0x03, 0x00, 0x01, 0x36, 0x28, 0x19,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, // IEND chunk
    0xAE, 0x42, 0x60, 0x82,
  ];

  for (final name in ['app_icon.png', 'app_icon_foreground.png', 'splash_icon.png']) {
    File('assets/icons/$name').writeAsBytesSync(png);
  }

  print('✓ Placeholder icons created in assets/icons/');
  print('  Replace with your actual app icon before publishing!');
}
